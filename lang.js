// تهيئة اللغة الافتراضية
let currentLang = 'ar';

// دالة لتحميل النصوص بناءً على اللغة المحددة
function loadLanguage(lang) {
    currentLang = lang;
    const textObject = lang === 'ar' ? arabicText : englishText;
    
    // تحديث اتجاه الصفحة
    document.body.classList.remove('lang-ar', 'lang-en');
    document.body.classList.add(`lang-${lang}`);
    document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    
    // تحديث جميع العناصر التي تحتوي على معرف
    Object.keys(textObject).forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            element.textContent = textObject[id];
        }
    });
    
    // تحديث زر تبديل اللغة
    const langSwitch = document.getElementById('lang-switch');
    if (langSwitch) {
        const span = langSwitch.querySelector('span');
        if (span) {
            span.textContent = lang === 'ar' ? 'English' : 'العربية';
        }
    }
    
    // حفظ اللغة المفضلة في localStorage
    localStorage.setItem('preferredLang', lang);
}

// تحميل اللغة عند بدء التشغيل
document.addEventListener('DOMContentLoaded', function() {
    // التحقق من اللغة المحفوظة
    const savedLang = localStorage.getItem('preferredLang') || 'ar';
    loadLanguage(savedLang);
    
    // إضافة حدث النقر على زر تبديل اللغة
    const langSwitch = document.getElementById('lang-switch');
    if (langSwitch) {
        langSwitch.addEventListener('click', function() {
            const newLang = currentLang === 'ar' ? 'en' : 'ar';
            loadLanguage(newLang);
        });
    }
});